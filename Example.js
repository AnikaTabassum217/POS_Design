

// BURGER

function incrementBurger() {
    var countElement = document.getElementById("burgerCount");
    var burgerCount = parseInt(countElement.textContent);
    burgerCount++;
    countElement.textContent = burgerCount;
}
function decrementBurger() {
    var countElement = document.getElementById("burgerCount");
    var burgerCount = parseInt(countElement.textContent);
    burgerCount--;
    countElement.textContent = burgerCount;
}

